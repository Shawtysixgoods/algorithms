def bubble_sort(arr):
    """
    Реализация пузырьковой сортировки.
    Сортирует массив arr по возрастанию.
    """
    n = len(arr)  # Узнаем длину массива
    
    # Проходим по массиву n раз (максимум n-1 итераций)
    for i in range(n):
        swapped = False  # Флаг, чтобы отслеживать, были ли перестановки
        
        # Внутренний цикл проходит до элемента, который уже на своем месте
        for j in range(0, n - i - 1):
            # Сравниваем текущий элемент с соседним
            if arr[j] > arr[j + 1]:
                # Если порядок неправильный, меняем элементы местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True  # Устанавливаем флаг, чтобы показать, что была перестановка
        
        # Если ни одной перестановки не было, массив уже отсортирован
        if not swapped:
            break
    
    return arr  # Возвращаем отсортированный массив

# Пример использования:
if __name__ == "__main__":
    # Массив для сортировки
    array = [64, 34, 25, 12, 22, 11, 90]
    
    print("Исходный массив:", array)
    sorted_array = bubble_sort(array)  # Сортируем массив
    print("Отсортированный массив:", sorted_array)
