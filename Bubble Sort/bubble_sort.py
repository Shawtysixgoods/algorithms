def bubble_sort(arr): # arr - это твой список чисел
    n = len(arr)      # n - сколько чисел в списке (у нас 5)

    # 1. Главный Проход (Внешний цикл)
    for i in range(n - 1):
        # Зачем? Чтобы повторить сравнение соседей НЕСКОЛЬКО РАЗ.
        # Почему n-1 раз? Столько проходов точно хватит, чтобы всё расставить.
        # i - это номер прохода (0, 1, 2, 3...)

        # 2. Сравнение Соседей (Внутренний цикл)
        for j in range(n - 1 - i):
            # Зачем? Чтобы пройти ПОЧТИ по всему списку и сравнить каждую пару соседей.
            # j - это место (индекс), где мы сейчас смотрим.
            # Почему n-1-i?
            #   n-1: Чтобы не вылезти за границу списка при сравнении j и j+1.
            #   -i: После каждого ГЛАВНОГО прохода (i), самый большой элемент уже "всплыл" в самый конец.
            #       Его больше трогать не надо. Этот "-i" делает цикл короче с каждым разом.

            # 3. Главное Сравнение
            if arr[j] > arr[j + 1]:
                # Если левый сосед БОЛЬШЕ правого...

                # 4. Обмен Местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                # ...то меняем их местами! Легкий способ в Python.

# Пример:
numbers = [5, 1, 4, 2, 8]
bubble_sort(numbers) # Запускаем нашу сортировку
print(numbers)       # Получаем: [1, 2, 4, 5, 8]