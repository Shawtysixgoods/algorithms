def bubble_sort(arr):
    n = len(arr)
    # Внешний цикл - количество проходов
    for i in range(n - 1):
        # Внутренний цикл - сравнение соседних элементов
        for j in range(n - 1 - i):
            if arr[j] > arr[j + 1]:
                # Меняем местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]

# Пример использования
numbers = [5, 1, 4, 2, 8]
bubble_sort(numbers)
print(numbers)  # Результат: [1, 2, 4, 5, 8]

# Внутренний цикл укорачивается (n - 1 - i) за счет того, что после каждого 
# прохода самый крупный элемент «уходит» в конец, и на следующих итерациях его уже не нужно трогать.