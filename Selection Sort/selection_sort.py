def selection_sort(arr):
    """
    Реализация алгоритма сортировки выбором.
    Сортирует массив arr по возрастанию.
    """

    # Проходим по всем элементам массива
    for i in range(len(arr)):
        # Предполагаем, что текущий индекс i содержит минимальный элемент
        min_index = i

        # Ищем минимальный элемент в оставшейся части массива
        for j in range(i + 1, len(arr)):
            if arr[j] < arr[min_index]:
                min_index = j  # Обновляем индекс минимального элемента

        # Если найден минимальный элемент, меняем его местами с текущим
        if min_index != i:
            arr[i], arr[min_index] = arr[min_index], arr[i]

    return arr  # Возвращаем отсортированный массив

# Пример использования
if __name__ == "__main__":
    array = [64, 25, 12, 22, 11]  # Исходный массив
    print("Исходный массив:", array)
    sorted_array = selection_sort(array)  # Сортируем массив
    print("Отсортированный массив:", sorted_array)
