def insertion_sort(arr):
    """
    Реализация алгоритма сортировки вставками.
    Сортирует массив arr по возрастанию.
    """

    # Начинаем цикл со второго элемента, так как первый элемент уже считается отсортированным
    for i in range(1, len(arr)):
        current_value = arr[i]  # Сохраняем текущий элемент, который нужно вставить
        position = i  # Начальная позиция текущего элемента

        # Проверяем, есть ли элементы в отсортированной части, которые больше текущего
        while position > 0 and arr[position - 1] > current_value:
            # Если такой элемент найден, сдвигаем его вправо
            arr[position] = arr[position - 1]
            position -= 1  # Сдвигаем указатель позиции на один шаг влево

        # Вставляем текущий элемент на правильное место
        arr[position] = current_value

    return arr  # Возвращаем отсортированный массив

# Пример использования
if __name__ == "__main__":
    array = [5, 3, 8, 6, 2]  # Исходный массив
    print("Исходный массив:", array)
    sorted_array = insertion_sort(array)  # Сортируем массив
    print("Отсортированный массив:", sorted_array)
