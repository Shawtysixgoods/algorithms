def insertion_sort(arr):
    n = len(arr)
    for i in range(1, n):
        key = arr[i]
        j = i - 1
        # Сдвигаем элементы arr[0..i-1], которые больше key
        # на одну позицию вперед
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        # Вставляем текущий элемент на освободившееся место
        arr[j + 1] = key

# Пример использования
numbers = [5, 2, 9, 1, 5, 6]
insertion_sort(numbers)
print(numbers)  # Результат: [1, 2, 5, 5, 6, 9]


# Идея в том, что key – это элемент, который мы вставляем. Мы двигаемся назад (по индексам j) 
# и сдвигаем все элементы, которые больше key, вправо, пока не найдем место, куда можно вставить key.